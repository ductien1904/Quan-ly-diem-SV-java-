    private void loadData() {
        
        model.setRowCount(0); 
        List<chuyennganh> ds = xuly.getALL();
        for (chuyennganh cn : ds) {
            model.addRow(new Object[]{
                cn.getId(), 
                cn.getMaChuyenNganh(), 
                cn.getTenChuyenNganh()
            });
        }
    }
