    private void txt_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_themActionPerformed
        // TODO add your handling code here:
        
        String ma = txt_macn.getText();
        String ten = txt_tencn.getText();

        if (ma.isEmpty() || ten.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đủ thông tin!");
            return;
        }

        
        chuyennganh cn = new chuyennganh(0, ma, ten);
        if (xuly.insert(cn) > 0) {
            JOptionPane.showMessageDialog(this, "Thêm thành công!");
            loadData(); 
            txt_macn.setText("");
            txt_tencn.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Lỗi! Mã chuyên ngành có thể đã tồn tại.");
        }
    }//GEN-LAST:event_txt_themActionPerformed

    private void txt_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_suaActionPerformed
        // TODO add your handling code here:
        int row = tbl_chuyennganh.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần sửa!");
            return;
        }

       
        int id = Integer.parseInt(model.getValueAt(row, 0).toString());
        String ma = txt_macn.getText();
        String ten = txt_tencn.getText();

        chuyennganh cn = new chuyennganh(id, ma, ten);
        
        if (xuly.update(cn)) {
            JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
            loadData();
        } else {
            JOptionPane.showMessageDialog(this, "Lỗi khi cập nhật!");
        }
    }//GEN-LAST:event_txt_suaActionPerformed

    private void txt_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_xoaActionPerformed
        // TODO add your handling code here:
        int row = tbl_chuyennganh.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần xóa!");
            return;
        }

        int choice = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (choice == JOptionPane.YES_OPTION) {
            int id = Integer.parseInt(model.getValueAt(row, 0).toString());
            
            if (xuly.delete(id)) {
                JOptionPane.showMessageDialog(this, "Xóa thành công!");
                loadData();
                txt_macn.setText("");
                txt_tencn.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Lỗi! Có thể chuyên ngành này đang có sinh viên học.");
            }
        }
    }//GEN-LAST:event_txt_xoaActionPerformed

