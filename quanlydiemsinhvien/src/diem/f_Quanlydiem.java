    private void loadTable() {
        DefaultTableModel model = (DefaultTableModel) tblDiem.getModel();
    model.setRowCount(0);
    List<Diem> list = diemdao.getAll();
    int stt = 1;
    for (Diem d : list) {
        model.addRow(new Object[]{
            stt++, 
            d.getMaSV(), 
            d.getTenSV(), 
            d.getMaLop(), 
            d.getMaMH(),  
            d.getTenMH(), 
            d.getDiemTP1(), 
            d.getDiemTP2(), 
            d.getDiemCuoiKi(), 
            d.getDiemTongKet()
        });
    }
    }
    private void loadCombos() {
    
    cboSinhVien.removeAllItems();
    cboMonHoc.removeAllItems();
    cboMaLop.removeAllItems();

    // Thêm dòng mặc định để người dùng dễ chọn (Index 0)
    cboSinhVien.addItem("--- Chọn Mã SV ---");
    cboMaLop.addItem("--- Chọn Mã Lớp ---");
    cboMonHoc.addItem("--- Chọn Mã Môn ---");

    try {
        // 2. Load danh sách Sinh Viên và Mã Lớp từ lớp xuly_sv
        xuly_sv svdao = new xuly_sv(); 
        List<SinhVien> dsSV = svdao.getALL(); 
        
        // Sử dụng Set để lọc các mã lớp trùng nhau
        java.util.Set<String> setMaLop = new java.util.HashSet<>();

        if (dsSV != null) {
            for (SinhVien sv : dsSV) {
                // Thêm Mã SV vào ComboBox
                cboSinhVien.addItem(sv.getMaSV());
                
                // Lấy mã lớp từ sinh viên và cho vào Set (Set sẽ tự loại bỏ trùng lặp)
                if (sv.getMaLop() != null && !sv.getMaLop().isEmpty()) {
                    setMaLop.add(sv.getMaLop());
                }
            }
        }

        // Đổ danh sách mã lớp đã lọc (không trùng) vào ComboBox Mã Lớp
        for (String ml : setMaLop) {
            cboMaLop.addItem(ml);
        }

        // 3. Load danh sách Môn Học từ lớp truyvandulieu_mh
        truyvandulieu_mh mhdao = new truyvandulieu_mh();
        List<MonHoc> dsMH = mhdao.getAll();
        
        if (dsMH != null) {
            for (MonHoc mh : dsMH) {
                cboMonHoc.addItem(mh.getMaMH());
            }
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Lỗi khi load dữ liệu vào ComboBox: " + e.getMessage());
        e.printStackTrace();
    }
}

