    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
DefaultTableModel model = (DefaultTableModel) tblDiem.getModel();
model.setRowCount(0);
List<Diem> listKetQua = new ArrayList<>();

// 2. Kiểm tra xem người dùng muốn tìm theo cái nào
if (cboSinhVien.getSelectedItem() != null && cboSinhVien.getSelectedIndex() > 0) {
    String maSV = cboSinhVien.getSelectedItem().toString();
    listKetQua = diemdao.getByMaSV(maSV);
    
} else if (cboMaLop.getSelectedItem() != null && cboMaLop.getSelectedIndex() > 0) {
    // Nếu không chọn SV mà chọn Lớp thì tìm theo Lớp
    String maLop = cboMaLop.getSelectedItem().toString();
    listKetQua = diemdao.getByMaLop(maLop);
    
} else {
    JOptionPane.showMessageDialog(this, "Vui lòng chọn Mã Sinh Viên hoặc Mã Lớp để tìm kiếm!");
    return;
}

// 3. Đổ dữ liệu vào bảng
if (listKetQua == null || listKetQua.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Không tìm thấy dữ liệu điểm phù hợp!");
} else {
    int stt = 1;
    for (Diem d : listKetQua) {
        model.addRow(new Object[]{
            stt++, 
            d.getMaSV(), 
            d.getTenSV(), 
            d.getMaLop(),        
            d.getMaMH(),  
            d.getTenMH(), 
            d.getDiemTP1(),      
            d.getDiemTP2(),      
            d.getDiemCuoiKi(),   
            d.getDiemTongKet()    
        });
    }
}
    }//GEN-LAST:event_btnTimKiemActionPerformed

